public class OperacionesConEntero {
    public static void main(String[] args) {
        int numero = 10; 

        System.out.println("Número inicial: " + numero);

        numero++;         
        numero += 80;     
        numero -= 35;     
        numero *= 15;     
        numero /= 4;      
        numero--;         

        System.out.println("Número final: " + numero);
    }
}
